    private void btnCuentaNuevaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCuentaNuevaActionPerformed
        String nCorreo = txtNuevoCorreo.getText();
        String nClave = new String(txtNuevaClave.getPassword());
        ConexionDB cx = new ConexionDB();
        //proceso para buscar si el usuario existe
        String buscar = "select * from login where correo='"+nCorreo+"'";
        Statement st;
        ResultSet rs;
        try {
            st = cx.conectar().createStatement();
            rs = st.executeQuery(buscar);
            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "El correo ya esta en uso");
            } else {
                //consulta para registrar usuario
        String sql = "insert into login (correo , passwd) VALUES('"+nCorreo+"','"+nClave+"')";
        PreparedStatement ps;
        try {
            ps = cx.conectar().prepareStatement(sql);
            ps.executeUpdate();
            setVisible(false);
            JOptionPane.showMessageDialog(null, "Te has registrado con exito");
        } catch (SQLException e) {
            System.out.println(e);
        } //fin de crear usuario
            }
        } catch (SQLException e) {
            System.out.println(e);
        }

    }//GEN-LAST:event_btnCuentaNuevaActionPerformed

